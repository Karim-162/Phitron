#include<stdio.h>
int main()
{
    int a[2]; // মাত্র দুটি সংখ্যা প্রয়োজন
    int count = 0;

    // দুটি সংখ্যা ইনপুট নেওয়া
    for(int i = 0; i < 2; i++) {
        scanf("%d", &a[i]);
    }

    // শর্ত অনুযায়ী সংখ্যা পরিবর্তন করা
    while(a[0] <= a[1]) {
        a[0] *= 3; // প্রথম সংখ্যাকে ৩ দিয়ে গুণ
        a[1] *= 2; // দ্বিতীয় সংখ্যাকে ২ দিয়ে গুণ
        count++;   // ধাপ গণনা
    }

    // ফলাফল প্রিন্ট করা
    printf("%d\n", count);

    return 0;
}
