#include <stdio.h>

int main() {
    int a, b, c;
    scanf("%d %d %d", &a, &b, &c); // তিনটি সংখ্যা ইনপুট নেওয়া

    int *p1 = &a, *p2 = &b, *p3 = &c; // প্রতিটি ভেরিয়েবলের জন্য পয়েন্টার ব্যবহার

    int sum = *p1 + *p2 + *p3; // পয়েন্টার ব্যবহার করে যোগফল বের করা

    printf("%d\n", sum); // যোগফল প্রিন্ট করা

    return 0;
}
